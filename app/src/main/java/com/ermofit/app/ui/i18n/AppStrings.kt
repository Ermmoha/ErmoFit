val LocalAppStrings = staticCompositionLocalOf { ruStrings }
val LocalAppLanguage = staticCompositionLocalOf { AppLanguage.RU }

@Composable
fun ProvideAppStrings(
    language: AppLanguage,
    content: @Composable () -> Unit
) {
    val resolvedLanguage = when (language) {
        AppLanguage.SYSTEM -> {
            val localeCode = java.util.Locale.getDefault().language.lowercase()
            if (localeCode.startsWith("ru")) AppLanguage.RU else AppLanguage.EN
        }
        else -> language
    }
    val strings = when (resolvedLanguage) {
        AppLanguage.RU -> ruStrings
        AppLanguage.EN -> enStrings
        AppLanguage.SYSTEM -> ruStrings
    }
    CompositionLocalProvider(
        LocalAppStrings provides strings,
        LocalAppLanguage provides resolvedLanguage,
        content = content
    )
}

@Composable
fun appStrings(): AppStrings = LocalAppStrings.current

@Composable
fun appLanguage(): AppLanguage = LocalAppLanguage.current

fun AppStrings.validationMessage(error: AuthValidationError): String {
    return when (error) {
        AuthValidationError.NAME_REQUIRED -> validationNameRequired
        AuthValidationError.NAME_TOO_SHORT -> validationNameShort
        AuthValidationError.NAME_TOO_LONG -> validationNameLong
        AuthValidationError.NAME_INVALID_CHARS -> validationNameChars
        AuthValidationError.EMAIL_REQUIRED -> validationEmailRequired
        AuthValidationError.EMAIL_TOO_LONG -> validationEmailLong
        AuthValidationError.EMAIL_INVALID -> validationEmailInvalid
        AuthValidationError.PASSWORD_REQUIRED -> validationPasswordRequired
        AuthValidationError.PASSWORD_TOO_SHORT -> validationPasswordShort
        AuthValidationError.PASSWORD_TOO_LONG -> validationPasswordLong
        AuthValidationError.PASSWORD_HAS_SPACES -> validationPasswordSpaces
        AuthValidationError.PASSWORD_NEEDS_UPPER -> validationPasswordUpper
        AuthValidationError.PASSWORD_NEEDS_LOWER -> validationPasswordLower
        AuthValidationError.PASSWORD_NEEDS_DIGIT -> validationPasswordDigit
        AuthValidationError.PASSWORD_NEEDS_SPECIAL -> validationPasswordSpecial
        AuthValidationError.REPEAT_PASSWORD_REQUIRED -> validationRepeatPasswordRequired
        AuthValidationError.PASSWORDS_MISMATCH -> validationPasswordsMismatch
        AuthValidationError.LOGIN_PASSWORD_TOO_SHORT -> validationLoginPasswordShort
    }
}

fun AppStrings.authErrorMessage(error: AuthError): String {
    return when (error) {
        AuthError.INVALID_CREDENTIALS -> authInvalidCredentials
        AuthError.INVALID_EMAIL -> authInvalidEmail
        AuthError.WRONG_PASSWORD -> authWrongPassword
        AuthError.WEAK_PASSWORD -> authWeakPassword
        AuthError.USER_NOT_FOUND -> authUserNotFound
        AuthError.USER_DISABLED -> authUserDisabled
        AuthError.USER_COLLISION -> authUserCollision
        AuthError.NETWORK_ERROR -> authNetworkError
        AuthError.TOO_MANY_REQUESTS -> authTooManyRequests
        AuthError.CONFIGURATION_NOT_FOUND -> authConfigurationError
        AuthError.UNKNOWN -> authUnknownError
    }
}

